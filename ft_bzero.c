#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*tmp;
	size_t	i;

	i = 0;
	tmp = (char *)s;
	if (n == 0)
		return ;
	while (n > i)
	{
		*tmp = 0;
		tmp++;
		n--;
	}
}
